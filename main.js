function getPageContent(page) {
  let contentToReturn;
  switch (page) {
    case "contact":
      contentToReturn = pages.contact;
      break;
    case "education":
      contentToReturn = pages.education;
      break;
    case "experience":
      contentToReturn = pages.experience;
      break;
    case "knowledge":
      contentToReturn = pages.knowledge;
      break;
    case "tools":
      contentToReturn = pages.tools;
      break;
    default:
      contentToReturn = pages.contact;
      break;
  }
  document.getElementById("content").innerHTML = contentToReturn;

  // Agregar clase 'active' al botón seleccionado
  const navLinks = document.querySelectorAll(".nav-link");
  navLinks.forEach((link) => {
    link.classList.remove("active");
    selectedLink.classList.add("btn-outline-dark");
  });
  const selectedLink = document.querySelector(
    `[onclick="getPageContent('${page}')"]`
  );
  selectedLink.classList.add("active");
  link.classList.remove("btn-outline-dark");
  link.classList.remove("btn-dark");
}
